class UserModel {
  final String name;
  final String surname;
  final String imageAsset;
  final int score;
  final String joinDate;
  final String address;
  final String email;

  UserModel({
    required this.name,
    required this.surname,
    required this.imageAsset,
    required this.score,
    required this.joinDate,
    required this.address,
    required this.email,
  });
}
